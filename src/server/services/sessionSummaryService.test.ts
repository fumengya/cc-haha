import { afterEach, describe, expect, it } from 'vitest'
import {
  type SessionSummary,
  _buildRecentRawSliceForTest,
  formatHandoffSystemPrompt,
} from './sessionSummaryService'

const ENV_KEYS = [
  'CLAUDE_CODE_HANDOFF_RAW_TURNS',
  'CLAUDE_CODE_HANDOFF_RAW_CHARS',
] as const

afterEach(() => {
  for (const key of ENV_KEYS) {
    delete process.env[key]
  }
})

const baseSummary: SessionSummary = {
  sessionId: 'sess-1',
  generatedAt: '2026-06-10T12:00:00.000Z',
  baseMessageCount: 42,
  modelUsed: 'anthropic:claude-haiku-4',
  main: 'PROJECT-LEVEL SUMMARY GOES HERE.',
  recent: 'RECENT TURNS SUMMARY.',
}

describe('formatHandoffSystemPrompt', () => {
  it('keeps the existing layout when no recentRaw is present (back-compat for old caches)', () => {
    const out = formatHandoffSystemPrompt(baseSummary)
    expect(out).toContain('# Hand-off context from the previous session')
    expect(out).toContain('## Project-level summary')
    expect(out).toContain('PROJECT-LEVEL SUMMARY GOES HERE.')
    expect(out).toContain('## Most recent decisions and state')
    expect(out).toContain('RECENT TURNS SUMMARY.')
    // No raw section header when the field is absent — so legacy summaries
    // generated by v0.5.7 keep producing the same prompt bytes.
    expect(out).not.toContain('Most recent messages (verbatim, truncated)')
    expect(out).toContain('(Generated from session sess-1, 42 messages')
  })

  it('appends a verbatim raw block when recentRaw is present', () => {
    const summary: SessionSummary = {
      ...baseSummary,
      recentRaw: ['USER: please review the diff', 'ASSISTANT: Sure, on it.'].join('\n\n'),
    }
    const out = formatHandoffSystemPrompt(summary)
    expect(out).toContain('## Most recent messages (verbatim, truncated)')
    expect(out).toContain('USER: please review the diff')
    expect(out).toContain('ASSISTANT: Sure, on it.')
    // The raw block sits AFTER the abstract recent summary so the model
    // reads context-then-detail.
    const recentIdx = out.indexOf('RECENT TURNS SUMMARY.')
    const rawIdx = out.indexOf('USER: please review the diff')
    expect(recentIdx).toBeGreaterThan(0)
    expect(rawIdx).toBeGreaterThan(recentIdx)
  })

  it('renders raw block inside a fenced code block so markdown does not collapse formatting', () => {
    const summary: SessionSummary = { ...baseSummary, recentRaw: 'USER: line one\n\nASSISTANT: line two' }
    const out = formatHandoffSystemPrompt(summary)
    // Fence must wrap the raw lines so the AI sees verbatim role markers
    // even if its rendering layer interprets markdown.
    const fenceOpens = out.split('```').length - 1
    expect(fenceOpens).toBeGreaterThanOrEqual(2)
  })
})

describe('buildRecentRawSlice', () => {
  it('keeps the trailing N turns when there are more turns than the cap', () => {
    process.env.CLAUDE_CODE_HANDOFF_RAW_TURNS = '3'
    process.env.CLAUDE_CODE_HANDOFF_RAW_CHARS = '10000'
    const turns = [
      'USER: turn-0',
      'ASSISTANT: reply-0',
      'USER: turn-1',
      'ASSISTANT: reply-1',
      'USER: turn-2',
      'ASSISTANT: reply-2',
    ]
    const out = _buildRecentRawSliceForTest(turns)
    expect(out).not.toContain('turn-0')
    expect(out).not.toContain('reply-0')
    expect(out).toContain('turn-2')
    expect(out).toContain('reply-2')
  })

  it('returns the entire transcript when it is shorter than the tail cap', () => {
    process.env.CLAUDE_CODE_HANDOFF_RAW_TURNS = '20'
    const turns = ['USER: hello', 'ASSISTANT: hi']
    expect(_buildRecentRawSliceForTest(turns)).toBe('USER: hello\n\nASSISTANT: hi')
  })

  it('truncates a long single turn and preserves the role prefix', () => {
    process.env.CLAUDE_CODE_HANDOFF_RAW_TURNS = '1'
    const longBody = 'x'.repeat(5_000)
    const out = _buildRecentRawSliceForTest([`USER: ${longBody}`])
    expect(out.startsWith('USER: ')).toBe(true)
    // Per-turn cap is 400 chars in the implementation. After cap we expect
    // an ellipsis, and the body should be much shorter than the input.
    expect(out.length).toBeLessThan(500)
    expect(out.endsWith('…')).toBe(true)
  })

  it('drops older turns first when the total cap is busted by dense recent turns', () => {
    process.env.CLAUDE_CODE_HANDOFF_RAW_TURNS = '10'
    process.env.CLAUDE_CODE_HANDOFF_RAW_CHARS = '1200'
    // 500-char body × 3 turns busts the per-turn 400-char cap, so each
    // line becomes ~400 chars after truncation. 3 × ~400 + 2 separators
    // ≈ 1204 chars > 1200 cap → oldest drops.
    const dense = 'y'.repeat(500)
    const turns = [
      `USER: oldest-${dense}`,
      `ASSISTANT: middle-${dense}`,
      `USER: newest-${dense}`,
    ]
    const out = _buildRecentRawSliceForTest(turns)
    expect(out).toContain('newest-')
    expect(out).toContain('middle-')
    expect(out).not.toContain('oldest-')
  })

  it('returns empty string on empty input or when CLAUDE_CODE_HANDOFF_RAW_TURNS=0', () => {
    expect(_buildRecentRawSliceForTest([])).toBe('')
    process.env.CLAUDE_CODE_HANDOFF_RAW_TURNS = '0'
    expect(_buildRecentRawSliceForTest(['USER: x', 'ASSISTANT: y'])).toBe('')
  })
})
